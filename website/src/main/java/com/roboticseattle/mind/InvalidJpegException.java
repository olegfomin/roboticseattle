package com.roboticseattle.mind;

public class InvalidJpegException extends Exception {

	public InvalidJpegException() {
	}

	public InvalidJpegException(String arg0) {
		super(arg0);
	}

	public InvalidJpegException(Throwable arg0) {
		super(arg0);
	}

	public InvalidJpegException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public InvalidJpegException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
